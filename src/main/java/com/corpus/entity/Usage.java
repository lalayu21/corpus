package com.corpus.entity;

public class Usage {
	private int id;
	
	private int setID;
	
	private String user;
	
	private String time;
	
	private String result;
	
	private String desp;
	
	private String ip;
	
	private String linuxUser;
	
	private String linuxPaw;
	
	private String wavePath;
	
	private String labelPath;
	
	private int flag;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getSetID() {
		return setID;
	}

	public void setSetID(int setID) {
		this.setID = setID;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getDesp() {
		return desp;
	}

	public void setDesp(String desp) {
		this.desp = desp;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getLinuxUser() {
		return linuxUser;
	}

	public void setLinuxUser(String linuxUser) {
		this.linuxUser = linuxUser;
	}

	public String getLinuxPaw() {
		return linuxPaw;
	}

	public void setLinuxPaw(String linuxPaw) {
		this.linuxPaw = linuxPaw;
	}

	public String getWavePath() {
		return wavePath;
	}

	public void setWavePath(String wavePath) {
		this.wavePath = wavePath;
	}

	public String getLabelPath() {
		return labelPath;
	}

	public void setLabelPath(String labelPath) {
		this.labelPath = labelPath;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}
	
}
